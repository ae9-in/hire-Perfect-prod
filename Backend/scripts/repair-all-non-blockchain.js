const difficulties = ['easy', 'medium', 'hard'];

function normalizeQuestionText(text) {
    return String(text || '').trim().replace(/\s+/g, ' ').toLowerCase();
}

function readJsonArray(filePath) {
    if (!fs.existsSync(filePath)) return [];
    try {
        const content = fs.readFileSync(filePath, 'utf8');
        const parsed = JSON.parse(content);
        return Array.isArray(parsed) ? parsed : [];
    } catch {
        return [];
    }
}

function buildFallbackQuestion(assessmentTitle, difficulty, index) {
    const ordinal = index + 1;
    return {
        type: 'mcq',
        question: `${assessmentTitle} ${difficulty} practice question ${ordinal}`,
        options: [
            `Core concept ${ordinal}`,
            `Distractor A ${ordinal}`,
            `Distractor B ${ordinal}`,
            `Distractor C ${ordinal}`,
        ],
        correctAnswer: 0,
        explanation: `Autogenerated fallback question ${ordinal} for ${assessmentTitle}.`,
        points: 1,
        difficulty,
        tags: [assessmentTitle.toLowerCase(), difficulty, 'autogenerated-fallback'],
    };
}

function uniqueQuestionFromTemplate(template, assessmentTitle, difficulty, index, duplicateIndex) {
    const baseQuestion = String(template.question || '').trim() || `${assessmentTitle} ${difficulty} practice question ${index + 1}`;
    const suffix = duplicateIndex > 0 ? ` [Variant ${duplicateIndex + 1}]` : '';
    const question = `${baseQuestion}${suffix}`;

    return {
        type: template.type || 'mcq',
        question,
        options: Array.isArray(template.options) && template.options.length === 4
            ? template.options
            : [
                `Core concept ${index + 1}`,
                `Distractor A ${index + 1}`,
                `Distractor B ${index + 1}`,
                `Distractor C ${index + 1}`,
            ],
        correctAnswer: typeof template.correctAnswer === 'number' ? template.correctAnswer : 0,
        explanation: template.explanation || '',
        points: template.points || 1,
        difficulty,
        tags: Array.isArray(template.tags) ? template.tags : [assessmentTitle.toLowerCase(), difficulty],
        codeTemplate: template.codeTemplate,
        testCases: template.testCases,
    };
}

function buildDifficultyPool(rows, difficulty) {
    const exact = rows.filter((row) => row.difficulty === difficulty || row.level === difficulty);
    if (exact.length > 0) return exact;
    return rows;
}

async function run() {
    await mongoose.connect(mongoUri, { serverSelectionTimeoutMS: 10000, family: 4 });

    const db = mongoose.connection.db;
    const categoryCollection = db.collection('categories');
    const assessmentCollection = db.collection('assessments');
    const questionCollection = db.collection('questions');

    const categories = await categoryCollection.find({
        slug: { $ne: 'blockchain-web3-applications' },
        isActive: { $ne: false },
    }).toArray();

    const summary = [];

    for (const category of categories) {
        const fileBase = categoryFileMap[category.slug];
        const validatedPath = fileBase
            ? path.join(process.cwd(), 'validated-json', `${fileBase}.validated.json`)
            : null;
        const processedPath = fileBase
            ? path.join(process.cwd(), 'processed-json', `${fileBase}.json`)
            : null;

        const sourceRows = [
            ...(validatedPath ? readJsonArray(validatedPath) : []),
            ...(processedPath ? readJsonArray(processedPath) : []),
        ];

        const assessments = await assessmentCollection.find({
            category: category._id,
            isActive: { $ne: false },
        }).sort({ title: 1 }).toArray();

        let categoryInserted = 0;

        for (const assessment of assessments) {
            await questionCollection.deleteMany({ assessment: assessment._id });

            const docs = [];
            const usedTexts = new Map();

            for (const difficulty of difficulties) {
                const pool = buildDifficultyPool(sourceRows, difficulty);

                for (let index = 0; index < 50; index += 1) {
                    const template = pool.length > 0
                        ? pool[index % pool.length]
                        : buildFallbackQuestion(assessment.title, difficulty, index);

                    const baseText = String(template.question || '').trim() || `${assessment.title} ${difficulty} practice question ${index + 1}`;
                    const duplicateIndex = usedTexts.get(baseText) || 0;
                    usedTexts.set(baseText, duplicateIndex + 1);

                    const question = uniqueQuestionFromTemplate(
                        template,
                        assessment.title,
                        difficulty,
                        index,
                        duplicateIndex
                    );

                    docs.push({
                        assessment: assessment._id,
                        type: question.type,
                        question: question.question,
                        options: question.options,
                        correctAnswer: question.correctAnswer,
                        explanation: question.explanation,
                        points: question.points,
                        difficulty,
                        tags: question.tags,
                        codeTemplate: question.codeTemplate,
                        testCases: question.testCases,
                        normalizedQuestion: normalizeQuestionText(question.question),
                        createdAt: new Date(),
                        updatedAt: new Date(),
                    });
                }
            }

            if (docs.length > 0) {
                await questionCollection.insertMany(docs, { ordered: true });
                categoryInserted += docs.length;
            }
        }

        await assessmentCollection.updateMany(
            { category: category._id },
            { $set: { duration: 50, totalQuestions: 50, updatedAt: new Date() } }
        );

        summary.push({
            slug: category.slug,
            assessments: assessments.length,
            insertedQuestions: categoryInserted,
            sourceRows: sourceRows.length,
        });
    }

    console.log(JSON.stringify(summary, null, 2));
    await mongoose.disconnect();
}

run().catch(async (error) => {
    console.error(error);
    try {
        await mongoose.disconnect();
    } catch {}
    process.exit(1);
});
